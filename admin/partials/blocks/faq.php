    <h3 class="virgil-phe-global-page-title">FAQ</h3>
    <p class="virgil-phe-global-faq-question">Do users have to change their passwords if the database has been
        compromised?</p>
    <p class="virgil-phe-global-faq-answer">If a database has been stolen, users do not need to change their original
        passwords. However, you need to
        rotate all user records in your database. This will use cryptography to disconnect the compromised Pure records from the original passwords, leaving any unauthorized party empty handed.</p>
    <hr>
    <p class="virgil-phe-global-faq-question">How much does it cost?</p>
    <p class="virgil-phe-global-faq-answer">Pure is a FREE toolkit. All libraries are open source and can be found on GitHub, where they are available for free to any user.</p>
    <hr>
    <p class="virgil-phe-global-faq-question">What if an App Private Key gets lost?</p>
    <p class="virgil-phe-global-faq-answer">There is no way to restore the  APP_SECRET_KEY. The database becomes
        inaccessible and therefore useless. So, it makes sense to immediately make a backup of the key in any convenient form.</p>
